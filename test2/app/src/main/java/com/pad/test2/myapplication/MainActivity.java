    private TextView nameTextView;
    private GoogleApiClient googleApiClient;

    private FirebaseAuth firebaseAuth;

    private FirebaseAuth.AuthStateListener firebaseAuthListener;
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        requestWindowFeature(Window.FEATURE_NO_TITLE);
        getWindow().setFlags(WindowManager.LayoutParams.FLAG_FULLSCREEN,
                WindowManager.LayoutParams.FLAG_FULLSCREEN);
        setContentView(R.layout.activity_main);

        Button newGame = findViewById(R.id.newgame);
        newGame.setOnClickListener(new View.OnClickListener(){
            @Override
            public void onClick(View v){
                newGame();
            }
        });

        Button classification = findViewById(R.id.classification);
        classification.setOnClickListener(new View.OnClickListener(){
            @Override
            public void onClick(View v){
                newClassification();
            }
        });

        nameTextView = findViewById(R.id.name);
        GoogleSignInOptions gso = new Builder(DEFAULT_SIGN_IN)
                .requestEmail()
                .build();

        googleApiClient = new GoogleApiClient.Builder(this)
                .enableAutoManage(this, this)
                .addApi(Auth.GOOGLE_SIGN_IN_API, gso)
                .build();


        firebaseAuth = FirebaseAuth.getInstance();
        firebaseAuthListener = new FirebaseAuth.AuthStateListener() {
            @Override
            public void onAuthStateChanged(@NonNull FirebaseAuth firebaseAuth) {
                FirebaseUser user = firebaseAuth.getCurrentUser();
                if(user != null) {
                    setUserData(user);
                }else{
                    goLoginScreen();
                }
            }
        };
    }


    private void setUserData(FirebaseUser user) {
        String text = "Welcome " + user.getDisplayName();
        nameTextView.setText(text);
    }

    private void newGame() {
        Intent intent = new Intent(this, GameActivity.class);
        intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP | Intent.FLAG_ACTIVITY_CLEAR_TASK | Intent.FLAG_ACTIVITY_NEW_TASK);
        startActivity(intent);
    }

    private void newClassification() {
        Intent intent = new Intent(this, ClassificationActivity.class);
        intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP | Intent.FLAG_ACTIVITY_CLEAR_TASK | Intent.FLAG_ACTIVITY_NEW_TASK);
        startActivity(intent);
    }

